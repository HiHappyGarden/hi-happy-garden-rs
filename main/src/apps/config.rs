#[derive(Serialize, Deserialize, Clone, Copy)]
pub struct DaylightSavingTime {
    pub start_month: u8,
    pub start_day: u8,
    pub start_hour: u8,
    pub end_month: u8,
    pub end_day: u8,
    pub end_hour: u8,
    pub enabled: bool,
}

impl Default for DaylightSavingTime {
    fn default() -> Self {
        Self {
            start_month: DEFAULT_DAYLIGHT_SAVING_START_MONTH,
            start_day: DEFAULT_DAYLIGHT_SAVING_START_DAY,
            start_hour: DEFAULT_DAYLIGHT_SAVING_START_HOUR,
            end_month: DEFAULT_DAYLIGHT_SAVING_END_MONTH,
            end_day: DEFAULT_DAYLIGHT_SAVING_END_DAY,
            end_hour: DEFAULT_DAYLIGHT_SAVING_END_HOUR,
            enabled: DEFAULT_DAYLIGHT_SAVING_ENABLED,
        }
    }
}

impl DaylightSavingTime {
    pub const AT_CMD: &'static str = "AT+DST";
    pub const AT_RESP: &'static str = "+DST: ";
}

impl AtContext<{ CMD_SIZE }> for DaylightSavingTime {
    fn query(&mut self, at_response: &'static str) -> AtResult<'_, { CMD_SIZE }> {
        let mut response = Bytes::<{ CMD_SIZE }>::new();
        response.format(format_args!("{},{},{},{},{},{},{}",
            self.start_month, self.start_day, self.start_hour,
            self.end_month, self.end_day, self.end_hour,
            self.enabled as u8));
        Ok((at_response, response))
    }

    #[inline]
    fn test(&mut self, at_response: &'static str) -> AtResult<'_, { CMD_SIZE }> {
        Ok(at_cmd_response!(at_response; "start_month,<value> | start_day,<value> | start_hour,<value> | end_month,<value> | end_day,<value> | end_hour,<value> | enabled,<0|1>"))
    }

    fn set(&mut self, at_response: &'static str, args: at_parser_rs::Args) -> AtResult<'_, { CMD_SIZE }> {
        if StatusSignal::get() & <StatusFlag as Into<u32>>::into(StatusFlag::UserLogged) == 0 {
            return Err((at_response, AtError::Unhandled(NOT_LOGGED_RESPONSE)));
        }
        let cmd = args.get(0).ok_or((at_response, AtError::InvalidArgs))?;
        match cmd.as_ref() {
            "start_month" => 
                self.start_month = args.get(1).ok_or((at_response, AtError::InvalidArgs))?
                    .parse().map_err(|_| (at_response, AtError::InvalidArgs))?,
            
            "start_day" => 
                self.start_day = args.get(1).ok_or((at_response, AtError::InvalidArgs))?
                    .parse().map_err(|_| (at_response, AtError::InvalidArgs))?,

            "start_hour" => 
                self.start_hour = args.get(1).ok_or((at_response, AtError::InvalidArgs))?
                    .parse().map_err(|_| (at_response, AtError::InvalidArgs))?,

            "end_month" => 
                self.end_month = args.get(1).ok_or((at_response, AtError::InvalidArgs))?
                    .parse().map_err(|_| (at_response, AtError::InvalidArgs))?,

            "end_day" => 
                self.end_day = args.get(1).ok_or((at_response, AtError::InvalidArgs))?
                    .parse().map_err(|_| (at_response, AtError::InvalidArgs))?,
            
            "end_hour" => 
                self.end_hour = args.get(1).ok_or((at_response, AtError::InvalidArgs))?
                    .parse().map_err(|_| (at_response, AtError::InvalidArgs))?,
            
            "enabled" => {
                let value: u8 = args.get(1).ok_or((at_response, AtError::InvalidArgs))?
                    .parse().map_err(|_| (at_response, AtError::InvalidArgs))?;
                self.enabled = value != 0;
            }
            _ => return Err((at_response, AtError::InvalidArgs)),
        }

        Config::shared().apply_daylight_saving_time();

        Ok(at_cmd_response!(at_response; ""))
    }
}

#[derive(Serialize, Deserialize, Clone, Copy)]
pub struct WifiConfig {
    ssid: Bytes<32>,
    password: Bytes<32>,
    auth: Auth,
    enabled: bool,
}

impl Default for WifiConfig {
    fn default() -> Self {
        Self {
            ssid: Bytes::from_str(DEFAULT_WIFI_SSID),
            password: Bytes::from_str(DEFAULT_WIFI_PASSWORD),
            auth: DEFAULT_WIFI_AUTH.into(),
            enabled: DEFAULT_WIFI_ENABLED,
        }
    }
}

impl WifiConfig {
    pub const AT_CMD: &'static str = "AT+WIFI";
    pub const AT_RESP: &'static str = "+WIFI: ";

    pub fn get_ssid(&self) -> &Bytes<32> {
        mutex().lock();
        let ssid = &self.ssid;
        mutex().unlock();
        ssid
    }

    pub fn get_password(&self) -> &Bytes<32> {
        mutex().lock();
        let password = &self.password;
        mutex().unlock();
        password
    }

    pub fn is_enabled(&self) -> bool {
        mutex().lock();
        let enabled = self.enabled;
        mutex().unlock();
        enabled
    }

    pub fn get_auth(&self) -> Auth {
        mutex().lock();
        let auth = self.auth;
        mutex().unlock();
        auth
    }

    pub fn set_ssid(&mut self, ssid: Bytes<32>) {
        mutex().lock();
        self.ssid = ssid;
        mutex().unlock();
    }

    pub fn set_password(&mut self, password: Bytes<32>) {
        mutex().lock();
        self.password = password;
        mutex().unlock();
    }

    pub fn set_enabled(&mut self, enabled: bool) {
        mutex().lock();
        self.enabled = enabled;
        mutex().unlock();
    }

    pub fn set_auth(&mut self, auth: Auth) {
        mutex().lock();
        self.auth = auth;
        mutex().unlock();
    }
}

impl AtContext<{ CMD_SIZE }> for WifiConfig {
    
    #[inline]
    fn query(&mut self, at_response: &'static str) -> AtResult<'_, { CMD_SIZE }> {
        Ok(at_cmd_response!(at_response; quoted!(self.ssid.as_str()), <Auth as Into<u8>>::into(self.auth), self.enabled as u8))
    }

    #[inline]
    fn test(&mut self, at_response: &'static str) -> AtResult<'_, { CMD_SIZE }> {
        Ok(at_cmd_response!(at_response; "<ssid>,<password>,<auth(0-6)>,<enabled(0|1)>"))
    }

    fn set(&mut self, at_response: &'static str, args: at_parser_rs::Args) -> AtResult<'_, { CMD_SIZE }> {
        if StatusSignal::get() & <StatusFlag as Into<u32>>::into(StatusFlag::UserLogged) == 0 {
            return Err((at_response, AtError::Unhandled(NOT_LOGGED_RESPONSE)));
        }
        let ssid = args.get(0).ok_or((at_response, AtError::InvalidArgs))?;
        if ssid.len() > 32 {
            return Err((at_response, AtError::Unhandled("ssid max len 32")));
        }
        let password = args.get(1).ok_or((at_response, AtError::InvalidArgs))?;
        if password.len() > 32 {
            return Err((at_response, AtError::Unhandled("password max len 32")));
        }
        let auth: u8 = args.get(2).ok_or((at_response, AtError::InvalidArgs))?
            .parse().map_err(|_| (at_response, AtError::InvalidArgs))?;
        let enabled: u8 = args.get(3).ok_or((at_response, AtError::InvalidArgs))?
            .parse().map_err(|_| (at_response, AtError::InvalidArgs))?;

        self.ssid = Bytes::from_str(ssid.as_ref());
        self.password = Bytes::from_str(password.as_ref());
        self.auth = Auth::from(auth);
        self.enabled = enabled != 0;

        Config::shared().apply_wifi();

        Ok(at_cmd_response!(at_response; ""))
    }
}

