static mut MUTEX: Option<RawMutex> = None;

const fn mutex() -> &'static RawMutex {
    unsafe {
        match &*&raw const MUTEX {
            Some(mutex) => mutex,
            None => panic!("MUTEX_HANDLER is not initialized"),
        }
    }
}

