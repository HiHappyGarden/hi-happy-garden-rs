#[derive(Serialize, Deserialize, Clone, Copy)]
pub struct Config {
    version: u8,
    serial: Bytes<16>,
    timezone: i16,
    daylight_saving_time: DaylightSavingTime,
    session: Session,
    wifi: WifiConfig,
    ntp: NtpConfig,
}

impl Default for Config {
    fn default() -> Self {
        Self {
            version: 0,
            serial: Bytes::new(),
            timezone: DEFAULT_TIMEZONE,
            daylight_saving_time: Default::default(),
            session: Default::default(),
            wifi: Default::default(),
            ntp: Default::default(),
        }
    }
}

impl Initializable for Config {
    fn init(&mut self) -> Result<()> {
        log_info!(APP_TAG, "Init app config");

        unsafe {
            MUTEX = Some(RawMutex::new()?);
        }

        self.session.init()?;

        let config = Self::load()?;
        config.apply_locale();
        config.apply_daylight_saving_time();
        config.apply_ntp();
        config.apply_wifi();
        config.apply_session();


        Ok(())
    }
}

impl AtContext<{ CMD_SIZE }> for Config {
    fn exec(&mut self, at_response: &'static str) -> AtResult<'_, { CMD_SIZE }> {
        Config::save().map_err(|_| (at_response, AtError::Unhandled("Save error")))?;
        Ok(at_cmd_response!(at_response; ""))
    }

    fn query(&mut self, at_response: &'static str) -> AtResult<'_, { CMD_SIZE }> {
        Ok(at_cmd_response!(at_response; quoted!(self.serial.as_str()), self.timezone))
    }

    #[inline]
    fn test(&mut self, at_response: &'static str) -> AtResult<'_, { CMD_SIZE }> {
        Ok(at_cmd_response!(at_response; "serial,<value> | timezone,<value> | save | load"))
    }

    fn set(&mut self, at_response: &'static str, args: at_parser_rs::Args) -> AtResult<'_, { CMD_SIZE }> {
        if StatusSignal::get() & <StatusFlag as Into<u32>>::into(StatusFlag::UserLogged) == 0 {
            return Err((at_response, AtError::Unhandled(NOT_LOGGED_RESPONSE)));
        }
        let cmd = args.get(0).ok_or((at_response, AtError::InvalidArgs))?;
        match cmd.as_ref() {
            "serial" => {
                let value = args.get(1).ok_or((at_response, AtError::InvalidArgs))?;
                if value.len() > 16 {
                    return Err((at_response, AtError::Unhandled("serial max len 16")));
                }
                self.serial = Bytes::from_str(value.as_ref());
            }
            "timezone" => {
                let value: i16 = args.get(1).ok_or((at_response, AtError::InvalidArgs))?
                    .parse().map_err(|_| (at_response, AtError::InvalidArgs))?;
                self.timezone = value;
                self.apply_locale();
            }
            "save" => {
                Config::save().map_err(|_| (at_response, AtError::Unhandled("Save error")))?;
            }
            "load" => {
                let config = Config::load().map_err(|_| (at_response, AtError::Unhandled("Load error")))?;
                config.apply_locale();
                config.apply_daylight_saving_time();
                config.apply_ntp();
                config.apply_wifi();
                config.apply_session();
            }
            _ => return Err((at_response, AtError::InvalidArgs)),
        }
        Ok(at_cmd_response!(at_response; ""))
    }
}

impl Config {
    const FILE_NAME: &'static str = "config.json";
    pub const AT_CMD: &'static str = "AT+CNF";
    pub const AT_RESP: &'static str = "+CNF: ";

    /// Initialize Config with default values from CMake
    fn with_defaults() -> Self {
        let mut config = Self::default();

        // Apply CMake defaults to WiFi config
        config.wifi = Default::default();
        // Apply CMake defaults to general config
        config.timezone = DEFAULT_TIMEZONE;
        config.daylight_saving_time = Default::default();

        // Set system user (position 0) from CMake defaults
        let _ = config.session.set_system_user(DEFAULT_SYSTEM_USER_EMAIL, DEFAULT_SYSTEM_USER_PASSWORD);

        config
    }

    pub fn apply_locale(&self) {
        DateTime::set_timezone(self.timezone);
    }

    pub fn apply_daylight_saving_time(&self) {
        DateTime::set_daylight_saving_time(
            self.daylight_saving_time.enabled,
            self.daylight_saving_time.start_month,
            self.daylight_saving_time.start_day,
            self.daylight_saving_time.start_hour,
            self.daylight_saving_time.end_month,
            self.daylight_saving_time.end_day,
            self.daylight_saving_time.end_hour,
        );
    }

    pub fn apply_ntp(&self) {
        Network::set_ntp(
            self.ntp.get_server().clone(),
            self.ntp.get_port(),
            self.ntp.get_msg_len(),
        );
    }

    pub fn apply_wifi(&self) {
        Wifi::set_config(
            self.wifi.get_ssid().clone(),
            self.wifi.get_password().clone(),
            self.wifi.get_auth(),
            self.wifi.is_enabled(),
        );
    }

    pub fn apply_session(&self) {
        self.session.set_user_local();
    }

    pub const fn shared() -> &'static mut Self {
        unsafe { &mut *&raw mut CONFIG }
    }

    pub fn load() -> Result<&'static mut Self> {
        let mut file_name = FileBytes::from_str(FS_CONFIG_DIR);
        file_name.append_str(FS_SEPARATOR_DIR);
        file_name.append_str(Config::FILE_NAME);

        let mut file = match Filesystem::open_with_as_sync_str(
            &file_name,
            flags::RDWR | flags::CREAT,
        ) {
            Ok(file) => file,
            Err(e @ Error::ReturnWithCode(-2)) => {
                log_info!(APP_TAG, "Failed to open config file: {e}, try to create it");
                Filesystem::open_with_as_sync_str(
                    &file_name,
                    flags::WRONLY | flags::CREAT,
                )?
            }
            Err(e) => return Err(e),
        };

        let wifi_json = file.read_with_as_sync_str(true)?;

        // If file is empty or doesn't exist, use defaults
        if wifi_json.is_empty() {
            log_info!(APP_TAG, "Config file not found or empty, using defaults");

            unsafe {
                CONFIG = Self::with_defaults();
            }

            Self::save()?;

            return Ok(unsafe { &mut *&raw mut CONFIG });
        }

        let wifi_json = match String::from_utf8(wifi_json) {
            Ok(json) => json,
            Err(e) => {
                return Err(Error::UnhandledOwned(format!(
                    "Failed to parse config JSON: {e}"
                )));
            }
        };


        match from_json::<Config>(&wifi_json) {
            Ok(config) => {
                unsafe {
                    CONFIG = config;
                }

                log_info!(APP_TAG, "Config loaded successfully");
                Ok(unsafe { &mut *&raw mut CONFIG })
            }
            Err(e) => {
                log_warning!(APP_TAG, "Using default config values err: {e}");
                unsafe { CONFIG = Self::with_defaults() };

                Self::save()?;

                Ok(unsafe { &mut *&raw mut CONFIG })
            }
        }
    }

    pub fn save() -> Result<&'static mut Self> {
        let mut file_name = FileBytes::from_str(FS_CONFIG_DIR);
        file_name.append_str(FS_SEPARATOR_DIR);
        file_name.append_str(Config::FILE_NAME);

        unsafe {
            to_json(&*&raw const CONFIG)
                .map_err(|e| {
                    Error::UnhandledOwned(format!("Failed to serialize config to JSON: {e}"))
                })
                .and_then(|json| {
                    let json_bytes = json.into_bytes();

                    let mut file = match Filesystem::open_with_as_sync_str(
                        &file_name,
                        flags::WRONLY | flags::CREAT,
                    ) {
                        Ok(file) => file,
                        Err(e @ Error::ReturnWithCode(-2)) => {
                            log_info!(APP_TAG, "Failed to open config file: {e}, try to create it");
                            Filesystem::open_with_as_sync_str(
                                &file_name,
                                flags::WRONLY | flags::CREAT,
                            )?
                        }
                        Err(e) => return Err(e),
                    };

                    file.write(&json_bytes, true)?;

                    log_info!(APP_TAG, "Config saved successfully");
                    Ok(&mut *&raw mut CONFIG)
                })
        }
    }

    pub fn get_version(&self) -> u8 {
        mutex().lock();
        let version = self.version;
        mutex().unlock();
        version
    }

    pub fn get_serial(&self) -> &Bytes<16> {
        mutex().lock();
        let serial = &self.serial;
        mutex().unlock();
        serial
    }

    pub fn set_serial(&mut self, serial: Bytes<16>) {
        mutex().lock();
        self.serial = serial;
        mutex().unlock();
    }

    pub fn get_timezone(&self) -> i16 {
        mutex().lock();
        let timezone = self.timezone;
        mutex().unlock();
        timezone
    }

    pub fn set_timezone(&mut self, timezone: i16) {
        mutex().lock();
        self.timezone = timezone;
        mutex().unlock();
    }

    pub fn get_ntp_config(&self) -> &NtpConfig {
        mutex().lock();
        let ntp = &self.ntp;
        mutex().unlock();
        ntp
    }

    pub fn get_ntp_config_mut(&mut self) -> &mut NtpConfig {
        mutex().lock();
        let ntp = &mut self.ntp;
        mutex().unlock();
        ntp
    }

    pub fn get_daylight_saving_time(&mut self) -> &mut DaylightSavingTime {
        mutex().lock();
        let dst = &mut self.daylight_saving_time;
        mutex().unlock();
        dst
    }

    pub fn get_wifi_config(&mut self) -> &mut WifiConfig {
        mutex().lock();
        let wifi = &mut self.wifi;
        mutex().unlock();
        wifi
    }

    pub fn get_session(&mut self) -> &mut Session {
        mutex().lock();
        let session = &mut self.session;
        mutex().unlock();
        session
    }
}
